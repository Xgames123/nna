.arch "nna8v2"
.org 0x00

; print the char in r1: to the screen afterwards returns to the address in r0:
print_char_loop:
; r1 offset on the screen
; r3 offset in the current character data

; load the char_addr into r0
lih &char_a.high
lil &char_a.low
;brk

mco add
cal r0 r3

mrd r2 r0 ; r2 = current pixel

lil 0xE
lih 0xF
mdb r0
xor r0 r0

cal r0 r1 ; r0+r1
mwr r2 r0

xor r0 r0
mdb r0

; check if r3 is a multiple of 4
brk
mov r2 r3 ; save r3 in r2
mco mod
lil 0x4
lih 0x0
;brk
cal r3 r0
;brk
dec r0 0x1
eq r0 r3
mov r3 r2 ;  load saved r3 back
brk
flf
bra &not_a_multiple_of_four.low .reachable &not_a_multiple_of_four
multiple_of_four:
;brk
lil 0xB
lih 0x0
mco add
cal r1 r0
not_a_multiple_of_four:
clf ; clear flag if branch was not taken

inc r3 0x1
inc r1 0x1
bra &no_overflow.low

; reset r2 and r3
xor r2 r2
xor r3 r3
; jump back to the start
lil 0x0
lih 0x0
jmp r0

no_overflow:
clf ; clear flag if branch was not taken

lil &print_char_loop.low
lih &print_char_loop.high
jmp r0



